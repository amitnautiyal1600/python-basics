data_string = "Amit Nautiyal"

print('String is "{}" with a length of  : {}'.format(data_string, len(data_string)))


#  Slice string from start 0 to index 3 ( last index is ignored - 4 th is ignored)
print(data_string[0:4])

#  slice from start to  4 same as upper

print(data_string[:4])

#  Slice string from middle  from 5th position to last ( if num is not defaine it will take to last digit )

print(data_string[5:])


#  slice a string from last

print(data_string[-5:])


